package Entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Embeddable;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

/**
 *
 * @author ahmed.ibraheem
 */
@Embeddable
public class ComponentPK implements Serializable {

    @Basic(optional = false)
    @Column(name = "component_ID", nullable = false)
    private int componentID;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 45)
    @Column(name = "name", nullable = false, length = 45)
    private String name;

    public ComponentPK() {
    }

    public ComponentPK(int componentID, String name) {
        this.componentID = componentID;
        this.name = name;
    }

    public int getComponentID() {
        return componentID;
    }

    public void setComponentID(int componentID) {
        this.componentID = componentID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (int) componentID;
        hash += (name != null ? name.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ComponentPK)) {
            return false;
        }
        ComponentPK other = (ComponentPK) object;
        if (this.componentID != other.componentID) {
            return false;
        }
        if ((this.name == null && other.name != null) || (this.name != null && !this.name.equals(other.name))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Entities.ComponentPK[ componentID=" + componentID + ", name=" + name + " ]";
    }
    
}
